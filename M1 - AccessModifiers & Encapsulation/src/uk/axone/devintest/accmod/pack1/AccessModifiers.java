package uk.axone.devintest.accmod.pack1;

public class AccessModifiers {

    //variables with public access
    public int i;
    public String strPub;

    //variables with private access
    private int j;
    private String strPri;

    //variables with default access
    int d;
    String str;

    //variables with protected access
    protected int p;
    protected String strPro;

    //public
    public void publicMethod(){

    }

    //private
    private void privateMethod(){

    }

    //protected
    protected void protectedMethod(){

    }

    //default
    void defaultMethod(){

    }
}
